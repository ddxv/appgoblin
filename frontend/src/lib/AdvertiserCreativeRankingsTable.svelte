<script lang="ts" generics="TData, TValue">
	import {
		type PaginationState,
		type SortingState,
		type ColumnFiltersState,
		getCoreRowModel,
		getPaginationRowModel,
		getSortedRowModel,
		getFilteredRowModel
	} from '@tanstack/table-core';

	import Pagination from '$lib/components/data-table/Pagination.svelte';
	import ExportAsCSV from '$lib/components/data-table/ExportAsCSV.svelte';
	import type { RankedApps } from '../types';

	import { createSvelteTable, FlexRender } from '$lib/components/data-table/index.js';

	import { genericColumns } from '$lib/components/data-table/generic-column';

	type DataTableProps<RankedApps, TValue> = {
		data: RankedApps[];
	};

	let pagination = $state<PaginationState>({ pageIndex: 0, pageSize: 10 });
	let sorting = $state<SortingState>([]);
	let columnFilters = $state<ColumnFiltersState>([]);

	let globalFilter = $state<string>('');

	let { data }: DataTableProps<RankedApps, TValue> = $props();

	const columns = genericColumns([
		{
			title: 'Advertiser',
			accessorKey: 'advertiser_name',
			isSortable: true
		},
		{
			title: 'Top Creatives',
			accessorKey: 'top_md5_hashes',
			isSortable: true
		},

		{
			title: 'MMP',
			accessorKey: 'mmp_domains',
			isSortable: true
		},
		{
			title: 'Unique Creatives',
			accessorKey: 'unique_creatives',
			isSortable: true
		},
		{
			title: 'Unique Publishers',
			accessorKey: 'unique_publishers',
			isSortable: true
		},
		{
			title: 'Last Seen',
			accessorKey: 'last_seen',
			isSortable: true
		}
	]);

	const globalFilterFn = (row: any, columnId: string, filterValue: string) => {
		const name = row.original.advertiser_name?.toLowerCase() ?? '';
		const query = filterValue.toLowerCase();
		return name.includes(query);
	};

	const table = createSvelteTable({
		get data() {
			return data;
		},
		columns,
		state: {
			get pagination() {
				return pagination;
			},
			get sorting() {
				return sorting;
			},
			get columnFilters() {
				return columnFilters;
			},
			get globalFilter() {
				return globalFilter;
			}
		},

		getSortedRowModel: getSortedRowModel(),

		globalFilterFn,

		onSortingChange: (updater) => {
			if (typeof updater === 'function') {
				sorting = updater(sorting);
			} else {
				sorting = updater;
			}
		},
		onColumnFiltersChange: (updater) => {
			if (typeof updater === 'function') {
				columnFilters = updater(columnFilters);
			} else {
				columnFilters = updater;
			}
		},

		onPaginationChange: (updater) => {
			if (typeof updater === 'function') {
				pagination = updater(pagination);
			} else {
				pagination = updater;
			}
		},
		onGlobalFilterChange: (updater) => {
			globalFilter = typeof updater === 'function' ? updater(globalFilter) : updater;
		},
		getCoreRowModel: getCoreRowModel(),
		getPaginationRowModel: getPaginationRowModel(),
		getFilteredRowModel: getFilteredRowModel()
	});
</script>

<div class="table-container">
	<div class="flex items-center p-2">
		<input
			placeholder="Filter apps..."
			value={globalFilter}
			oninput={(e) => {
				const value = e.currentTarget.value;
				table.setGlobalFilter(value);
			}}
			class="max-w-sm p-1"
		/>
	</div>
	<div class="overflow-x-auto pl-0">
		<table class="table table-hover table-auto w-full">
			<thead>
				{#each table.getHeaderGroups() as headerGroup (headerGroup.id)}
					<tr>
						{#each headerGroup.headers as header (header.id)}
							<th class="">
								{#if !header.isPlaceholder}
									<FlexRender
										content={header.column.columnDef.header}
										context={header.getContext()}
									/>
								{/if}
							</th>
						{/each}
					</tr>
				{/each}
			</thead>
			<tbody>
				{#each table.getRowModel().rows as row (row.id)}
					<tr class="px-0">
						<td class="text-xs md:text-base">
							<a href="/apps/{row.original.advertiser_store_id}/ad-placements">
								<div class="col-1">
									<img
										src={row.original.advertiser_icon_url_512}
										alt={row.original.advertiser_name}
										class="w-12 md:w-24 h-auto object-cover rounded"
										referrerpolicy="no-referrer"
									/>
								</div>
								{row.original.advertiser_name}
							</a>
						</td>
						<td>
							<div class="overvlow-y-auto">
								<div class="grid grid-cols-2 gap-1">
									{#each row.original.top_md5_hashes.slice(0, 4) as md5_hash}
										<a href="/apps/{row.original.advertiser_store_id}/ad-placements">
											<img
												src="https://media.appgoblin.info/creatives/thumbs/{md5_hash}.jpg"
												alt="Creative for {row.original.advertiser_name}"
												class="object-cover rounded"
											/>
										</a>
									{/each}
								</div>
							</div>
						</td>
						<td>{row.original.mmp_domains}</td>
						<td>{row.original.unique_creatives}</td>
						<td>{row.original.unique_publishers}</td>
						<td>{row.original.last_seen}</td>
					</tr>
				{/each}
			</tbody>
		</table>
		<footer class="flex justify-between">
			<div class="flex items-center justify-end space-x-2 py-4">
				<Pagination tableModel={table} />
				<ExportAsCSV {table} filename="appgoblin_apps" />
			</div>
		</footer>
	</div>
</div>
